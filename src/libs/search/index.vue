<template>
  <div
    class="group relative p-0.5 rounded-xl border-white duration-500 hover:bg-red-100/40"
    ref="containerTarget"
  >
    <div>
      <!-- 搜索图标 -->
      <m-svg-icon
        class="w-1.5 h-1.5 absolute translate-y-[-50%] top-[50%] left-2"
        name="search"
        color="#707070"
      ></m-svg-icon>

      <!-- 输入框 -->
      <input
        v-model="inputValue"
        @keyup.enter="onSearchHandler"
        class="block w-full h-[44px] pl-4 outline-0 bg-zinc-100 caret-zinc-400 rounded-xl text-zinc-900 tracking-wide text-sm font-semibold border-zinc-100 focus:border-red-300 group-hover:bg-white group-hover:border-zinc-200"
        type="text"
        placeholder="搜索"
        @focus="onFocusHandler"
        @blur="onBlurHandler"
      />

      <!-- 删除按钮 -->
      <m-svg-icon
        v-show="inputValue"
        class="h-1.5 w-1.5 absolute translate-y-[-50%] top-[50%] right-9 cursor-pointer duration-500"
        name="input-delete"
        @click="onClearClick"
      ></m-svg-icon>

      <!-- 分割线 -->
      <div
        class="opacity-0 h-1.5 w-[1px] absolute translate-y-[-50%] top-[50%] right-[62px] duration-500 bg-zinc-200 group-hover:opacity-100"
      ></div>

      <!-- 按钮（待做通用组件 -->
      <!--搜索按钮在.hover时展示 -->
      <m-button
        class="opacity-0 absolute translate-y-[-50%] top-[50%] right-1 rounded-full duration-900 group-hover:opacity-100"
        icon="search"
        iconColor="#ffffff"
        @click="onSearchHandler"
      ></m-button>
    </div>

    <!-- 下拉区 -->
    <transition name="slide">
      <div
        v-if="$slots.dropdown"
        v-show="isFocus"
        class="max-h-[368px] w-full text-base overflow-auto bg-white absolute z-20 left-0 top-[56px] p-2 rounded border border-zinc-200 duration-200 hover:shadow-2xl"
      >
        <slot name="dropdown"></slot>
      </div>
    </transition>
  </div>
</template>

<script>
// 6.事件处理
// 数据双向绑定
const EMIT_UPDATE_MODELVALUE = 'update:modelValue';
// 搜索
const EMIT_SEARCH = 'search';
// 删除所有文本
const EMIT_CLEAR = 'clear';
// 输入事件
const EMIT_INPUT = 'input';
// 获取焦点
const EMIT_FOCUS = 'focus';
// 失去焦点
const EMIT_BLUR = 'blur';
</script>

<script setup>
import { onClickOutside, useVModel } from '@vueuse/core';
import { ref, watch } from 'vue';
// 1.输入内容实现双向数据绑定
const props = defineProps({
  modelValue: {
    required: true,
    type: String
  }
});
const inputValue = useVModel(props);
const emits = defineEmits([
  EMIT_UPDATE_MODELVALUE,
  EMIT_SEARCH,
  EMIT_CLEAR,
  EMIT_INPUT,
  EMIT_FOCUS,
  EMIT_BLUR
]);

// 用户输入行为
watch(inputValue, (val) => {
  emits(EMIT_INPUT, val);
});

// 3.一键清空文本功能
const onClearClick = () => {
  inputValue.value = '';
  emits(EMIT_CLEAR, '');
};
// 4.触发搜索
const onSearchHandler = () => {
  emits(EMIT_SEARCH, inputValue.value);
  // console.log('ss');
};
// 5.控制下拉展示区的展示
// input是否获取焦点
const isFocus = ref(false);
const containerTarget = ref(null);
// search框内点击 显示下拉框
const onFocusHandler = () => {
  isFocus.value = true;
  emits(EMIT_FOCUS);
};
// 点击框外位置 隐藏下拉框
onClickOutside(containerTarget, () => {
  isFocus.value = false;
});
// 失去焦点
const onBlurHandler = () => {
  emits(EMIT_BLUR);
};

// 6.事件处理
</script>

<style lang="scss" scoped>
.slide-enter-active,
.slide-leave-active {
  transition: all 0.5s;
}

.slide-enter-from,
.slide-leave-to {
  opacity: 0;
  transform: translateY(40px);
}
</style>
